function reverse1(Str) {  
    let reverStr = ""
    for(let i = 0 ; i< Str.length ; i++ ){ 
        reverStr =  Str[i] + reverStr;
    }
    return reverStr ;
}
console.log(reverse1('yousef'));


function reverse2(Str) {  
    let reverStr = ""
    for(let char of Str ){ 
        reverStr =  char + reverStr;
    }
    return reverStr ;
}
console.log(reverse2('yousef'));


function reverse3(str) { 
    const StrToArray = str.split('');
    StrToArray.reverse(); 
    return StrToArray.join(''); 
}

console.log(reverse3('yousef'));



function reverse4(str) { 
    return str.split('').reverse().join('') ;
}

console.log(reverse4('yousef'));
